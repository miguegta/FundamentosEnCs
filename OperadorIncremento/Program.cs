// Posfijo
int edad = 10;
Console.WriteLine(edad);
edad++;
Console.WriteLine(edad);
edad--;
Console.WriteLine(edad);

Console.WriteLine("-------------------------------");

// Prefijo
int a = 10;
Console.WriteLine(a);
Console.WriteLine(a++);
Console.WriteLine(a);

Console.WriteLine("b ->");

int b = 10;
Console.WriteLine(b);
Console.WriteLine(++b);
Console.WriteLine(b);

//Prioridad
//x++ x--
//++x --x
//* / %
//+ -

